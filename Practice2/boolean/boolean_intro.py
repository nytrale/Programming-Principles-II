
#  bool() оценивает значение как True/False
print(bool("Hello"))   # True: непустая строка
print(bool(15))        # True: ненулевое число


x = "Hello"
y = 15
print(bool(x))         
print(bool(y))         


print(bool(""))        
print(bool(0))        
print(bool([]))       
print(bool({}))       
print(bool(None))      
